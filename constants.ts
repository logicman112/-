const generatePhrases = (): Phrase[] => {
  const phrases: Phrase[] = [];
  const cats = ['인사', '식당', '질문', '쇼핑', '미팅', '일상', '여행', '병원', '공항', '호텔'];
  cats.forEach(cat => {
    const bank = REAL_PHRASES[cat];
    for (let lv = 1; lv <= 10; lv++) {
      for (let i = 0; i < 3; i++) {
        const base = bank[(lv - 1) * 3 + i];
        phrases.push({
          id: `p_${cat}_${lv}_${i}`,
          level: lv,
          category: cat,
          jp: base.jp,
          phonetic: base.ph,
          kr: base.kr,
          romaji: ''
        });
      }
    }
  });
  return phrases;
};

export const PHRASES: Phrase[] = generatePhrases();
