const generateWords = (): Word[] => {
  const words: Word[] = [];
  const categories = Object.keys(RAW_VOCAB);
  
  categories.forEach(cat => {
    const bank = RAW_VOCAB[cat];
    for (let lv = 1; lv <= 5; lv++) {
      for (let i = 0; i < 20; i++) {
        const bankIdx = (lv - 1) * 20 + i;
        if (bankIdx < bank.length) {
          const base = bank[bankIdx];
          words.push({
            id: `w_${cat}_${lv}_${i}`,
            category: cat,
            level: lv,
            jp: base.jp,
            phonetic: base.ph,
            kr: base.kr
          });
        }
      }
    }
  });

  return words;
};

